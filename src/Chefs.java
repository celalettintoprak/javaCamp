public class Chefs {
	int  age;
	String id, name, surname;
	double salary;
	
	public Chefs() {
		
	}
	
	public Chefs(String id, int age, String name, String surname, double salary) {
		super();
		this.id = id;
		this.age = age;
		this.name = name;
		this.surname = surname;
		this.salary = salary;
	}
}